 # 3- --------محاسبه ساعتی که بیشترین میانگین رو داره--------

# میانگین ساعت هارو (24 تا) حساب میکنیم
# حالا میگه از بین این 24 تا ساعت کدومش بیشترین میانگین رو داره؟
# پیدا کردن اندیسی که بیشترین مقدار را دارد

hour_mean = np.mean(data, axis=(1,2))
max_hour_mean = np.argmax(hour_mean)
print(f"ساعتی که بیشترین میانگین رو داره ساعت {max_hour_mean+1} هست")    # ساعتی که بیشترین میانگین رو داره ساعت 1 هست
 
 
 


# 4- --------محاسبه واریانس هر سنسور--------

# واریانس فقط سنسور (فقط یک Axis) رو میخوایم
# پس باید بقیه بعدها حذف بشن و همونی که میخوایم بمونه

sensor_variance = np.round(np.var(data, axis=(0,1)),1)
print("واریانس هر سنسور: ",sensor_variance)    # واریانس هر سنسور:  [101.8  92.7 104.7 101.2 101.4 110.5  98.2 102.6 103.8]
    
    
    
    
# 5- --------پیدا کردن سنسوری که بیشترین نوسان رو داشته--------

# نوسان = واریانس
# بین این 9 سنسور، کدام یکی بیشترین نوسان را دارد؟

sensor_variance = np.var(data, axis=(0,1))
max_sensor_variance = np.argmax(sensor_variance)
print(f"سنسوری که بیشترین نوسان رو داره: {max_sensor_variance+1}")    # سنسوری که بیشترین نوسان رو داره: 6




