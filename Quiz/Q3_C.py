 # 1- --------محاسبه میانگین هر سنسور در کل روز--------

# راه axis selection
# در numpy وقتی می‌خوای یک بُعد حذف بشه، روی آن بُعد باید aggregation انجام بدی (مثل mean)    
# اگر می‌خوای نتیجه برای "یک بُعد خاص" باشه، باید روی بقیه بُعدها میانگین بگیری    
# پس بُعد سنسور باید نگه داشته شود و دو بُعد دیگر (زمان) باید حذف شوند    
    # axis=0 -> ساعت \ axis=1 -> دقیقه \ axis=2 -> سنسور
    
# پس باید به تابع mean دوتا Axis رو بدیم و این دوتا axis که بهش دادیم رو حذف میکنه و axis سوم میمونه
# تابع mean در حالت عادی اینجوریه -> np.mean(array, axis=None)
# اگه بخوایم بیشتر از یک axis بهش بدیم -> np.mean(array, axis=(1,2))  و در این حالت axis 1 و 2 حذف میشن و 3 میمونه
# important!    axis=(1,2) درست      axis=1,2 غلط
# axis=int → حذف یک بُعد
# axis=(int, int, ...) → حذف چند بُعد همزمان
# output = بُعدهایی که باقی مانده‌اند


sensor_mean = np.round(np.mean(data, axis=(0,1)),1)    # این خط برای هر سنسور، همه مقادیرش در تمام ساعات و دقیقه‌ها را جمع می‌کند و میانگین می‌گیرد
# sensor_mean.shape --> 9   دو بعد دیگه حذف شدن
print("میانگین هر سنسور در یک روز: ",sensor_mean)      # میانگین هر سنسور در یک روز:  [50.  49.8 49.9 50.1 50.4 49.9 49.7 50.1 50.3]





# 2- --------محاسبه میانگین هر ساعت (روی همه سنسورها)--------

hour_mean = np.round(np.mean(data, axis=(1,2)),1)     # این خط یعنی برای هر ساعت، همه داده‌های 60 دقیقه × 9 سنسور را با هم میانگین بگیر
print("میانگین هر ساعت: ",hour_mean)
