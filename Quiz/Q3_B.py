# =================== [ Second Step ] ======================


# ------- مجموع فروش هر روز -------
everyday_sale = products.sum(axis=0)
# این یک آرایه خروجی میده که 30 تا المنت داره
# هر المنت مجموع ستونهای هر روز هست
# وقتی میگیم axis=0 یعنی روی ردیف افقی حرکت میکنه و ستونها رو باهم جمع میکنه
# result = [sum column1, sum column2, sum column3, ..., sum column 30]

print("مجموع فروش هر روز: ", everyday_sale)
# مجموع فروش هر روز:  [161 277 301 260 212 240 142 232 201 282 182 187 312 243 151 212 232 198 274 294 286 217 324 191 260 242 269 306 218 333]
print("============================")



# -------مجموع فروش هر محصول-------
product1 = sum(products[0,:])
print("فروش ماهانه محصول 1: ", product1)   # فروش ماهانه محصول 1:  1441
product2 = sum(products[1,:])
print("فروش ماهانه محصول 2: ", product2)   # فروش ماهانه محصول 2:  1412
product3 = sum(products[2,:])
print("فروش ماهانه محصول 3: ", product3)   # فروش ماهانه محصول 3:  1292
product4 = sum(products[3,:])
print("فروش ماهانه محصول 4: ", product4)   # فروش ماهانه محصول 4:  1494
product5 = sum(products[4,:])
print("فروش ماهانه محصول 5: ", product5)   # فروش ماهانه محصول 5:  1600
