# -------میانگین فروش روزانه هر محصول-------
product1_avg = int(sum(products[0,:])/30)
print("میانگین فروش روزانه محصول 1: ", product1_avg)    # میانگین فروش روزانه محصول 1:  48
product2_avg = int(sum(products[1,:])/30)
print("میانگین فروش روزانه محصول 2: ", product2_avg)    # میانگین فروش روزانه محصول 2:  47
product3_avg = int(sum(products[2,:])/30)
print("میانگین فروش روزانه محصول 3: ", product3_avg)    # میانگین فروش روزانه محصول 3:  43
product4_avg = int(sum(products[3,:])/30)
print("میانگین فروش روزانه محصول 4: ", product4_avg)    # میانگین فروش روزانه محصول 4:  49
product5_avg = int(sum(products[4,:])/30)
print("میانگین فروش روزانه محصول 5: ", product5_avg)    # میانگین فروش روزانه محصول 5:  53
print("============================")



# -------بیشترین فروش ثبت شده در کل ماتریکس-------
max_sale = products.max()
# اگه مثلا بگیم max(axis=0) میاد بیشترین فروش در هر ستون رو حساب میکنه
# و همچنین axis=1 بیشترین فروش در هر ردیف


print("بیشترین فروش ثبت شده در کل ماتریکس: ", max_sale)    # بیشترین فروش ثبت شده در کل ماتریکس:  99
print("============================")




# -------اندیس (روز و محصول) بیشترین فروش رو پیدا کن-------
shape = products.shape
a = np.argmax(products)
b = np.unravel_index(a,shape)
day = b[1]
product = b[0]

# argmax() --> اندیسِ خطیِ بیشترین مقدار را می‌دهد
# یعنی اگه ماتریس بصورت (5,30) باشه از 0 تا 149 اندیس داریم               
# این تابع میاد اول ماتریکس رو flat میکنه و بعد میگه المنتی که max هست عدد اندیسش چی هست               
# unravel_index (argmax, shape) --> همان اندیس خطی را تبدیل می‌کند به (سطر، ستون)


print(f"بیشترین میزان فروش در روز {day} برای محصول {product} میباشد")    # بیشترین میزان فروش در روز 12 برای محصول 1 میباشد
print("============================")





# =================== [ Third Step ] ======================

# 1- -------پیدا کردن روزی که بیشترین فروش کل رو داشتیم-------

# b = [row,column] = [product, day] --> b[0]=product | b[1]=day
print("روزی که بیشترین فروش را داشتیم: ",np.unravel_index(a,shape)[1])    # روزی که بیشترین فروش را داشتیم:  12
print("============================")



# 2- -------محصولی که کمترین فروش کل رو داشته-------

monthly_sale = products.sum(axis=1)
c = np.argmin(monthly_sale)
# max() --> مقدار بزرگ‌ترین عدد را میده
# argmax() --> اندیس بزرگ‌ترین عدد را می‌دهد

# max() <-> min()
# argmax() <-> argmin()

print(f"محصولی که کمترین فروش را در کل ماه داشته: محصول شماره {c+1}")    # محصولی که کمترین فروش را در کل ماه داشته: محصول شماره 3
print("============================")




 # 3- -------روزایی که مجموع فروش بیشتر از میانگین کل فروش بوده-------
 
monthly_average_sale = int(np.floor(np.mean(products.sum(axis=0))))
print("میانگین فروش در یک ماه: ",monthly_average_sale)    # میانگین فروش در یک ماه:  241
daily_sale = products.sum(axis=0)
more_than_avg_sale = []
for index,item in enumerate(daily_sale):
    if item>monthly_average_sale:
        more_than_avg_sale.append(index+1)
    
print("روزهایی که مجموع فروش از میانگین کل فروش ماهانه بیشتر بوده: ",more_than_avg_sale)     # روزهایی که مجموع فروش از میانگین کل فروش ماهانه بیشتر بوده: [2, 3, 4, 10, 13, 14, 19, 20, 21, 23, 25, 26, 27, 28, 30]
print("============================")




# 4- -------فروش هارو نرمال کن (بین 0 تا 1) - min max normalization-------

# min max Normalization --> (x - min) / (max - min)
# x = داده اصلی   |  max= بزرگترین مقدار داده ها  |  min= کوچکترین مقدار داده ها
# نرمال کردن یعنی بازه از 0 تا 1 باشه و هر عددی غیر از min و max یک عدد در بازه 0 و 1 باشه

daily_sale = products.sum(axis=0)
min_sale = daily_sale.min()
max_sale = daily_sale.max()
sale_range = max_sale - min_sale
normalization = np.round((daily_sale-min_sale) / sale_range, 1)    # نتیجه نهایی را تا یک رقم اعشار گرد کن
print("normalization: ",normalization)

