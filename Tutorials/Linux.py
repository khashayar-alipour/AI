# چگونه می‌توانیم تاریخ و زمان دقیق را در هر لحظه در ترمینال‌مان مشاهده کنیم


# date command
# برای نمایش تاریخ و زمان دقیق سیستم‌مان   
# همچنین می‌توانیم از این دستور برای تغییر و ست کردن زمان و تاریخ سیستم‌مان استفاده کنیم   
   # date  =>  Sun Oct  3(روز هفته) 09:44:29 +0330(time zone) 2021

   # -u option
   # نمایش تاریخ و زمان بصورت GMT(Greenwich Mean Time)/UTC(Coordinated Universal Time )   
   # date -u  => Sun Oct  3 06:23:49 UTC 2021

    # ما می‌توانیم خروجی این دستور را به کلی عوض کنیم   
    # مثلا بگوییم به جای Sun، به ما Sunday را نمایش دهد   
   # date "+%FORMAT"  => قالب کلی استفاده از فرمت ها
   
   
# FORMAT LIST

   # date "+%D"  => 10/03/25  mm/dd/yy
   # date "+%Y-%m-%d"  => 2025-10-03  yy-mm-dd
   # date "+%Y/%m/%d"  => 2025/10/03
   # date "+%A %B %d %T"  => Sunday October 03 13:47:38

   # %D
   # این فرمت تاریخ را به فرمت mm/dd/yy را نمایش می‌دهد      
   # %d
   # این فرمت روز ماه (که عددی بین 01 تا 31 است) را نمایش می‌دهد      
   # %a
   # این فرمت نام کوتاه روز هفته (یعنی از Sun تا Sat) را نمایش می‌دهد      
   # %A
   # این فرمت نام کامل روز هفته (یعنی از Sunday تا Saturday) را نمایش می‌دهد      
   # %h \ %b
   # این فرمت‌ها نام کوتاه ماه (یعنی از Jan تا Dec) را نمایش می‌دهند      
   # %B
   # این فرمت نام کامل ماه (یعنی از January تا December) را نمایش می‌دهد      
   # %m
   # این فرمت‌ عدد ماه (یعنی از 01 تا 12) را نمایش می‌دهد      
   # %y
   # این فرمت‌ دو رقم آخر سال (یعنی از 00 تا 99) را نمایش می‌دهد      
   # %Y
   # این فرمت‌ عدد 4 رقمی سال (یعنی مثلا 2021) را نمایش می‌دهد      
   # %T
   # این فرمت‌ ساعت را به فرمت 24 ساعتی (یعنی به فرمت HH:MM:SS) نمایش می‌دهد      
   # %H
   # این فرمت‌ فقط ساعت را نمایش می‌دهد      
   # %M
   # این فرمت‌ فقط دقیقه را نمایش می‌دهد     
   # %S
   # این فرمت‌ فقط ثانیه را نمایش می‌دهد     
   


# --date option
   # برای دیدن زمان‌های گذشته می‌توانیم از این آپشن استفاده کنیم   
   # فرض کنید می‌خواهیم 2 سال پیش همین موقع را بررسی کنیم   
   # date --date="2 year ago"  =>  Thu Oct  3 11:41:15 +0330 2023
   
   # یا اگر می‌خواستیم 5 ثانیه پیش را بررسی کنیم   
   # date --date="5 sec ago"  =>  Sun Oct  3 11:42:40 +0330 2025
   
   # یا اگر می‌خواستیم دیروز را بررسی کنیم   
   # date --date="yesterday"  =>  Sat Oct  2 11:45:33 +0330 2025
   
   # همچنین برای بررسی 2 ماه پیش   
   # date --date="2 month ago"  =>  Tue Aug  3 12:46:17 +0430 2025
   
   
   
# --set option
   # به کمک این آپشن می‌توانیم تاریخ و ساعت سیستم‌مان را تغییر دهیم   
   # توصیه می‌کنیم هرگز از این دستور استفاده نکنید   
   # مثلا اگر می‌خواستیم زمان و تاریخ سیستم‌مان را به زمان و تاریخ Tue Nov 13 15:23:34 PDT 2018 تغییر دهیم   
   # date --set="Tue Nov 13 15:23:34 PDT 2018"
   
   
   
# cal command
   # مشاهده تقویم داخل ترمینال   
   # این دستور برای مشاهده تقویم یک ماه موردنظر و یا یک سال موردنظرمان نیز کاربردی است   
   
   # cal => مشاهده ماه جاری بصورت کامل
   # cal -y => یک سال
   
   # cal -A 2
       # این آپشن به تعداد دلخواه‌مان ماه‌های بعد از ماه فعلی‌مان را نشان می‌دهد      
       # برای مثال فرض کنید می‌خواهیم دو ماه آینده را نیز ببینیم      
       
