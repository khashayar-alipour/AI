# ساختار کلی دستورات لینوکس به این صورته:
    # COMMAND [OPTIONS] [ARGUMENTS]

# لینوکس در هر 3 بخش بالا case sensitive هست

# COMMAND
# این قسمت دستوری است که به سیستم‌عامل داده می‌شود   
# هر دستور یک برنامه است که به سیستم‌عامل می‌گوید تا کار‌هایی را انجام دهد   

# OPTION
# در این قسمت، صفر یا تعدادی آپشن گذاشته می‌شود   
#  آپشن‌ها گاهی با یک خط تیره و گاهی با دو خط تیره نمایش داده می‌شوند   
# به کمک آپشن‌ها می‌توانیم از حالات مختلف یک دستور بهره ببریم   
#  به طور مثال دستور pwd --help راهنمای دستور pwd را نمایش می‌دهد   

# ARGUMENTS
# در این بخش، صفر یا تعدادی آرگومان برای COMMAND نوشته می‌شود   
# این آرگومان‌ها ورودی‌های مورد نیاز برای COMMAND هستند   
# مثلا در این دستور echo hello دستور اصلی Echo هست و آرگومان hello هست   




# LONG FORMAT \ SHORT FORMAT
# اغلب آپشن‌های دستورها دارای این دو حالت هستن   

# حالت short format با - شروع می‌شوند و تک حرفی هستند   
# برای مثال در دستور ls آپشن‌های -a و -l همگی در حالت short format هستند   
# بعضی آپشن‌ها تنها دارای حالت short format هستند، برای مثال در man page دستور ls آپشن -f به این صورت است   

# حالت long format با -- شروع می‌شوند و چند حرفی هستند   
# برای مثال در دستور ls آپشن --all در حالت long format است   
# برخی آپشن‌ها تنها دارای حالت long format هستند، برای مثال آپشن --author به این صورت است   
   
# تفاوت مهم این دو حالت در استفاده هم‌زمان آن‌هاست   
# برای مثال اگر در دستور ls اگر قصد استفاده همزمان از سه آپشن -l و -t و -r را داشتیم:   
   # ls -l -t -r
   # ls -ltr
# تنها در حالتی که قصد استفاده همزمان از چند آپشن short format را داشته باشیم می‌توانیم تک حروف آن‌ها را پشت هم بیاوریم   






