    # ls /name

# در صورت استفاده با آپشن t-، نتایج بر اساس زمان آخرین تغییرات (modification time) مرتب می‌شوند
    # ls -t  =>  a.py  b.py  c.py


# در صورت استفاده با این آپشن l-، نتایج دقیق‌تر نمایش داده می‌شوند
# جزئیاتی از قبیل یوزر سازنده فایل‌ها، دسترسی‌ و حجم فایل‌ها، زمان آخرین تغییرات روی فایل‌ها و ...
    # ls -l
        # total 48   =>   مجموع حجم بلاک‌های حافظه برای فایل‌ها
        #-rw-rw-rw-. 1 root root    14 Oct 19  2024 a.py
        # -rw-rw-rw-. 1 root root    13 Oct 19  2024 b.py
        
        # ستون اول مربوط به دسترسی فایل‌ها و دایرکتوری‌هاست        
        # ستون دوم تعداد هاردلینک‌ ها میباشد       
# ستون سوم و چهارم به ترتیب نام یوزر و نام گروه آن یوزری که فایل یا دایرکتوری را ساخته نمایش می‌دهد       
# ستون پنجم حجم فایل‌ها را به بایت نمایش می‌دهد       
# ستون ششم و هفتم و هشتم به ترتیب ماه و روز و ساعت آخرین تغییرات روی فایل‌ها       


 # آپشن h-  =>  مخفف human readable
# باید حتما این آپشن را در کنار آپشن -l استفاده کنید
# ستون پنجم را برای ما خواناتر می‌کند
    # ls -lh
        # حجم فایل‌ها بسیار خواناتر شده‌اند          
      # در این خروجی K به معنای کیلوبایت، M به معنای مگابایت و G به معنای گیگابایت          


# در صورت استفاده از دستور ls با آپشن a-، تمامی فایل‌ها نمایش داده می‌شود
# معمولا فایل‌ها و دایرکتوری‌هایی که با . شروع می‌شوند جزء فایل‌های مخفی سیستم به حساب می‌‌آیند و با ls به شما نمایش داده نمی‌شوند
    # ls -a  =>  .  ..  .linux  a.py  b.py  c.py


# دستور ls به صورت پیش‌فرض نتایج را به صورت الفبایی مرتب می‌کند
# در صورت استفاده از دستور ls با آپشن r-، نتایج برعکس می‌شوند
    # ls -r  =>  c.py  b.py  a.py
    
# آپشن R- به صورت بازگشتی (recursive) به تمامی دایرکتوری‌های درون دایرکتوری فعلی شما رفته 
# و محتویات درون هر یک را نیز به طور جداگانه‌ای نمایش می‌دهد
    # ls -R
        # .:
        # my_folder   a.py
        # ./my_folder:
        # a.txt   b.txt   c.txt


# امکان وارد کردن یک دستور با چند آپشن همزمان یا چند آپشن تلفیقی وجود داره
    # ls -l -t
    # ls -lt  =>  مشاهده نتایج به صورت دقیق و مرتب‌سازی شده بر اساس زمان آخرین تغییرات
    

# هرچی جلوی دستور ls میذاریم یک آرگومان یا یک آپشن هست
# یک آرگومان ثابت رو میشه به دستورهای مختلف داد و مشکلی هم پیش نمیاد
# مثلا آرگومان زیر وقتی به دستور cd میدیم دایرکتوری تغییر میکنه
# ولی وقتی به ls میدیم بدون تغییر دایرکتوری (از هر جایی که توش هستیم) میتونیم محتویات دارکتوری مقصد رو ببینیم
