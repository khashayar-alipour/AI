# cp (copy)

# از a.txt که در دایرکتوری فعلی‌مان وجود دارد در دایرکتوری test یک کپی به نام b.txt می‌سازد
    # cp a.txt test/b.txt
    
# از فایل a.txt در دایرکتوری test1 یک کپی به نام b.txt در دایرکتوری test2 می‌سازد
    # cp test1/a.txt test2/b.txt
    
# یک کپی از دایرکتوری dir1 به نام dir2 در دایرکتوری test می‌سازد
# کپی کردن کل دایرکتوری با محتوای داخلش
    # cp -r dir1/ test/dir2
    
# با این آپشن امکان مشاهده ریز به ریز جزئیات کپی شدن برای‌مان فراهم است
# این اطلاعات به ما نشان می‌دهد که دقیقا هر فایل از کدام مبدا، به کدام فایل در مقصد کپی شده است
    # cp -v a.txt b.txt


# mv (move) -> این دستورات برای فایل و دایرکتوری یکی هستن

# دستور زیر فایل file را از دایرکتوری dir1 را به دایرکتوری dir2 منتقل می‌کنند
    # mv dir1/file dir2/
    
# دستور زیر نام file1 را به file2 تغییر می‌دهد
    # mv file1 file2




# rm (remove) -> حذف فایلها و دایرکتوری

# دستور زیر فایل a.txt درون دایرکتوری test را حذف می‌کند
    # rm test/a.txt
    
# دستور زیر دایرکتوری test و همه فایل‌ها و دایرکتوری‌های داخلش را حذف می‌کند
    # rm -r test/








